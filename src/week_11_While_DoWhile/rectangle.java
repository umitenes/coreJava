public class rectangle {
    public static void main(String[] args) {
        int x=1, y=8;
        while(x<=y){
            System.out.println("******");
            x++;
        }
        System.out.println("_______________________");
        for (x=1; x<=y; x++){
            System.out.println("******");
        }



    }
}
